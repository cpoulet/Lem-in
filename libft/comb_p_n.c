#include "libft.h"

void	comb(int p, int n)
{
	if (!p || !n)
	{
		ft_printf("\n");
		return ;
	}
	while (n)
	{
		ft_printf("%d ", n);
		n--;
		p--;
		comb(p, n);
	}
}
